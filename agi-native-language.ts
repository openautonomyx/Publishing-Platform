  /**
   * Parse JSON response from Claude
   */
  private parseNativeContent(
    response: string,
    language: SupportedLanguage
  ): NativeContent {
    try {
      // Extract JSON from response
      const jsonMatch = response.match(/\{[\s\S]*\}/);
      if (!jsonMatch) {
        throw new Error("No JSON found in response");
      }

      const parsed = JSON.parse(jsonMatch[0]);

      const content: string = parsed.content || "";

      return {
        language,
        languageName: LANGUAGE_MAP[language],
        content: parsed.content || "",
        title: parsed.title || "",
        shortDescription: parsed.shortDescription || "",
        hashtags: parsed.hashtags || [],
        keywords: parsed.keywords || [],
        readingTime: Math.ceil(content.split(/\s+/).length / 200),
        characterCount: content.length,
        culturalAdaptations: parsed.culturalAdaptations,
      };
    } catch (error) {
      console.error(`Failed to parse response for ${language}:`, error);
      return {
        language,
        languageName: LANGUAGE_MAP[language],
        content: response,
        title: "Content Generated",
        shortDescription: "",
        hashtags: [],
        keywords: [],
        readingTime: 1,
        characterCount: response.length,
      };
    }
  }

  /**
   * Detect language of input text
   */
  async detectLanguage(text: string): Promise<SupportedLanguage | null> {
    const response = await this.client.messages.create({
      model: this.model,
      max_tokens: 100,
      messages: [
        {
          role: "user",
          content: `Detect the language of this text and respond with ONLY the language code (e.g., "en", "es", "fr"). Text: "${text.substring(0, 500)}"`,
        },
      ],
    });

    const detected = (
      response.content[0].type === "text" ? response.content[0].text : ""
    )
      .trim()
      .toLowerCase();

    return (detected as SupportedLanguage) || null;
  }

  /**
   * Translate to multiple languages while maintaining native fluency
   */
  async translateToLanguages(
    content: string,
    targetLanguages: SupportedLanguage[]
  ): Promise<Map<SupportedLanguage, string>> {
    const translations = new Map<SupportedLanguage, string>();

    for (const lang of targetLanguages) {
      const response = await this.client.messages.create({
        model: this.model,
        max_tokens: 1500,
        messages: [
          {
            role: "user",
            content: `Translate this content to native ${LANGUAGE_MAP[lang]} that would be written by a native speaker. Maintain tone and meaning. NO explanations, ONLY the native ${LANGUAGE_MAP[lang]} translation:\n\n${content}`,
          },
        ],
      });

      const translated =
        response.content[0].type === "text" ? response.content[0].text : "";
      translations.set(lang, translated);

      await new Promise((resolve) => setTimeout(resolve, 500));
    }

    return translations;
  }

